package itc298final.com.rssappocolypse;

import android.app.Activity;
import android.os.Bundle;
import android.util.Log;

public class AboutActivity extends Activity {

    LogoSprites logoSprites;

    ///////////////////////////////////////////////////////////////////////
    @Override
    protected void onCreate( Bundle savedInstanceState ) {
    /*/////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////*/

        super.onCreate( savedInstanceState );
        logoSprites = new LogoSprites( this );
        setContentView( logoSprites );

    }

    ///////////////////////////////////////////////////////////////////////
    @Override
    public void onPause() {
    /*/////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////*/

        super.onPause();
        logoSprites.pause();

    }

    ///////////////////////////////////////////////////////////////////////
    @Override
    public void onResume() {
    /*/////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////*/

        super.onResume();

        logoSprites.resume( this );
        Log.d( getResources().getString( R.string.notice ), getResources().getString( R.string.started ));

    }

}